export function getVideoId(exerciseName: string, videoUrl?: string | null): string | null {
  if (videoUrl) {
    // Extract YouTube ID from full URL
    const match = videoUrl.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([a-zA-Z0-9_-]{11})/);
    if (match) return match[1];
  }
  // Fuzzy match against map
  const name = exerciseName.trim();
  if (EXERCISE_VIDEO_MAP[name]) return EXERCISE_VIDEO_MAP[name];
  // Partial match
  for (const [key, id] of Object.entries(EXERCISE_VIDEO_MAP)) {
    if (name.includes(key) || key.includes(name)) return id;
  }
  return null;
}
